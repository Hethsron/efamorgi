import java.time.LocalDateTime;
import java.util.Objects;

/**
 * @class       User
 * @brief       Implements the domain model
 */
public class User {

    private long id;
    private String lastname;
    private String firstname;
    private String email;
    private String password;
    private String date;

    public User(String lastname, String firstname, String email, String password) {
        this.lastname = lastname;
        this.firstname = firstname;
        this.email = email;
        this.password = password;
        this.date = LocalDateTime.now().toString();
        this.id = lastname.hashCode() + firstname.hashCode() + email.hashCode() + password.hashCode() + date.hashCode();
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getLastname() {
        return lastname;
    }

    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    public String getFirstname() {
        return firstname;
    }

    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    public String getEmail() {
        return email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o)
            return true;
        if (o == null || getClass() != o.getClass())
            return false;
        User user = (User) o;
        return id == user.id && lastname.equals(user.lastname) && firstname.equals(user.firstname) && email.equals(user.email) && password.equals(user.password) && date.equals(user.date);
    }

    @Override
    public int hashCode() {
        return Objects.hash(id, lastname, firstname, email, password, date);
    }

    @Override
    public String toString() {
        return "User{" +
                "id=" + id +
                ", lastname='" + lastname + '\'' +
                ", firstname='" + firstname + '\'' +
                ", email='" + email + '\'' +
                ", password='" + password + '\'' +
                ", date='" + date + '\'' +
                '}';
    }

}
